
//Create a class called Employee whose objects are records for an employee. 
public class Person {
//Private String field for the name of the employee.
    private String name;
//Person constructor
    public Person(String name) {
        this.name = name;
    }
//String method
    public String getName() {
        return name;
    }
//Method to set the name of the employee.
    public void setName(String name) {
        this.name = name;
    }
}
