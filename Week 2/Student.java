public class Student {

  private String stdName;
  private int stdID;
  //-----------------------
public void setName(String stdName, int stdID) {
  this.stdName = stdName;
  this.stdID = stdID;
}
  //-----------------------
public String getStdName() {
  return stdName;
}  
public int getStdID() {
  return stdID;
}
}